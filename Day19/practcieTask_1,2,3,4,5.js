//Print numbers from 1 to 20 using a for loop.
console.log("Task_1_output");
for (let z = 1; z <= 20; z++) {

    console.log("Number:" + z)
}

//Print numbers from 10 down to 1 using a while loop.


console.log("Task_2_output");
let num = 10;
while (num >= 1) {

    console.log(num)
    num--
}

//Print even numbers from 2 to 20.
console.log("Task_3_output");
for (let i = 1; i <= 20; i++) {
    if (i % 2 == 0) {

        console.log(i);
    }
}

//Use do...while to print numbers 1 to 5.
console.log("Task_4_output");
let x = 1;
do {
    console.log(x);
    x++
} while (x <= 5)


//Skip the number 7 using continue in a loop.

console.log("Task_5_output");

for (let y = 1; y <= 10; y++) {
    if (y == 7) continue
    {
        console.log(y);

    }
}

