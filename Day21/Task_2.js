//Add and remove items using push, pop, shift, unshift.

let color = ["green", 'red', 'black','bule','yellow']

//push

color.push("brown");
console.log('After push',color);

//pop

color.pop()
console.log('After pop',color);

//shift

color.shift()
console.log('After shift',color);

//unshift
color.unshift('green')
console.log('After unshift',color);


